#!/usr/bin/env python3
"""Nexus Orchestrator — Project Generator.

Reads nexus-config.json and produces project-specific scaffolding:
  - docs/agents/TEAM.md
  - docs/agents/SKILL_MAP.md
  - docs/ARCHITECTURE.md (stub)
  - CLAUDE.md (project-specific)
  - .claude/agents/<persona>.md for each persona
  - .claude/hooks/.env with resolved env var values
  - Updates .claude/settings.json env section

Usage:
  python3 generate-project.py --config nexus-config.json --target /path/to/project
  python3 generate-project.py --interactive --target /path/to/project
"""

